const box1 = document.getElementById('box-1');
let currentOffset = 0;

setInterval(() => {
  currentOffset++;
  if (currentOffset >= box1.children.length) {
    currentOffset = 0;
  }
  box1.classList.add('move-right');
  setTimeout(() => {
    box1.appendChild(box1.children[0]);
    box1.classList.remove('move-right');
  }, 300);
}, 300); // 0.3초 간격으로 애니메이션을 트리거



